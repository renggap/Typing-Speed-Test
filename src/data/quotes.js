/**
 * Get quotes filtered by difficulty level
 * @param {string} difficulty - Difficulty level: 'easy', 'medium', or 'hard'
 * @returns {array} Array of quotes matching the specified difficulty
 */
export const getQuotesByDifficulty = (difficulty) => {
  if (!difficulty) return quotes;

  return quotes.filter(quote =>
    quote.difficulty.toLowerCase() === difficulty.toLowerCase()
  );
};

/**
 * Get all unique authors from the quotes database
 * @returns {string[]} Array of unique author names
 */
export const getAllAuthors = () => {
  const authors = quotes.map(quote => quote.author);
  return [...new Set(authors)];
};

/**
 * Get quotes by a specific author
 * @param {string} author - Author name to filter by
 * @returns {array} Array of quotes by the specified author
 */
export const getQuotesByAuthor = (author) => {
  if (!author) return quotes;

  return quotes.filter(quote =>
    quote.author.toLowerCase().includes(author.toLowerCase())
  );
};